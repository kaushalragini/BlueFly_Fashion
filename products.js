// gneder filter
let genderFilter = document.querySelector("#genderoption");
genderFilter.value = "male";
genderFilter.addEventListener("change", function (e) {
  e.preventDefault();
  if (genderFilter.value === "female") {
    window.location = "womenlanding.html";
  }
});

//condition filter
let conditionFilter = document.querySelector("#conditionoption");
conditionFilter.addEventListener("change", function (e) {
  let conditionValue = conditionFilter.value;
  let filteredData = null;
  if (conditionValue === "all") {
    filteredData = womensData;
  } else {
    filteredData = womensData.filter(function (val) {
      // console.log(val.condition);
      return conditionValue === val.condition;
    });
  }
  displayProducts(filteredData);
});

// category filter
let categoryFilter = document.querySelector("#categoryoption");
categoryFilter.addEventListener("change", function (e) {
  let categoryValue = categoryFilter.value;
  let categoryData = null;
  if (categoryValue === "all") {
    categoryData = womensData;
  } else {
    categoryData = womensData.filter(function (val) {
      return val.category === categoryValue;
    });
  }
  displayProducts(categoryData);
});

let relatedSearchFilter = document.querySelectorAll(".related_search > div");
relatedSearchFilter.forEach(function (val) {
  val.addEventListener("click", function (e) {
    let related_search_item = val.className;
    if (related_search_item === "rswc") {
      window.location = "womenlanding.html";
    } else if (related_search_item === "rsmc") {
      let filteredData = womensData.filter(function (val) {
        return val.category === "c";
      });
      displayProducts(filteredData);
    } else if (related_search_item === "rsma") {
      let filteredData = womensData.filter(function (val) {
        return val.category === "a";
      });
      displayProducts(filteredData);
    } else {
      let filteredData = womensData.filter(function (val) {
        return val.brand === related_search_item;
      });
      displayProducts(filteredData);
    }
  });
});
